gen = (ps, ang) =>{
  out = "";
  cos = Math.cos;
  sin = Math.sin;
  cosa = cos(ang);
  sina = sin(ang);
  ps.forEach(v=>{
    
    if(v[0].toLowerCase()==='z')out+=` ${v[0]}`
    else{
      if(v[3]&&v[4]&&v[5]&&v[6]){
        out+=` ${v[0]} ${ "${"+`${v[1]*cosa-v[2]*sina}+${v[3]}*${cosa}-${v[4]}*${sina}+${v[5]}`+"}" }  ${"${"+`${v[1]*sina+v[2]*cosa}+${v[3]}*${sina}+${v[4]}*${cosa}+${v[6]}`+"}"}`
      }else if(v[5]&&v[6]){
        out+=` ${v[0]} ${"${"+`${v[1]*cosa-v[2]*sina}+${v[5]}`+"}"}  ${"${"+`${v[1]*sina+v[2]*cosa}+${v[6]}`+"}"}`
      }else if(v[3]&&v[4]){
        out+=` ${v[0]} ${ "${"+`${v[1]*cosa-v[2]*sina}+${v[3]}*${cosa}-${v[4]}*${sina}`+"}" }  ${"${"+`${v[1]*sina+v[2]*cosa}+${v[3]}*${sina}+${v[4]}*${cosa}`+"}"}`
      }
      else out+=` ${v[0]} ${v[1]*cosa-v[2]*sina} ${v[1]*sina+v[2]*cosa}`
    }
  })
  //(\+-)|(-\+)
  //console.log(out);
  out = out.toString()
  .replace(/(\+-)|(-\+)/g,'-')
  .replace(/(\+\+)/g,'+')
  .replace(/(--)/g,'+')
  .replace(/\*1\+/g,'+')
  .replace(/\*1\-/g,'-')
  .replace(/\*1\}/g,'}')
  .replace(/[+-][a-zA-Z*/0-9().]+[*]0[+]/g,'+')
  .replace(/[+-][a-zA-Z*/0-9().]+[*]0[-]/g,'-')
  .replace(/[+-][a-zA-Z*/0-9().]+[*]0[}]/g,'}')
  //console.log(out);
  
  
  
  return out;
}

